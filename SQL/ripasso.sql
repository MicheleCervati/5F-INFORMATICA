/*creare la tabella squadre con due campi:
 * id int primary key auto_increment,
 * descrizione varchar(40) not null*/

create table fantacalcio.sqaudre(
id int primary key auto_increment,
descrizione varchar(40) not null
);

/*importare dalla tabella listone nella tabella squadre le squadre */
insert into fantacalcio.sqaudre(descrizione)
select distinct l.squadra from fantacalcio.listone l;

select * from fantacalcio.sqaudre s ;

/*modificare la struttura della tabella listone inserendo un attributo
 * id_squadra di tipo int not null *//
 
 alter table fantacalcio.listone add id_squadra int not null;

/*aggiornare il campo id_squadre assegnando il valore corretto presente nella
 * tabella sqaudre*/

update fantacalcio.listone l 
set l.id_squadra =( 
select s.id from fantacalcio.sqaudre s 
where s.descrizione = l.squadra);

/*eliminare attributo id_squadra dalla tabella listone*/

alter table fantacalcio.listone drop column id_squadra;

/*creare una tabella con seguenti attributi
 * id int primary key auto_increment
 * ruolo char(1)
 * nome varchar(40)
 * id_squadra int foreign key(id_squadra) references squadre(id) 
 * quotazione int*/

create table fantacalcio.giocatori(
id int primary key auto_increment,
ruolo char(1),
nome varchar(40),
quotazione int,
id_squadra int,
foreign key (id_squadra) references fantacalcio.sqaudre(id)
);

/*visualizzare media quotazioni per ruolo dei calciatori*/

select l.ruolo ,avg(quotazione) as media from fantacalcio.listone l group by l.ruolo; 

/*visualizzare solo quotazioni media inferiori a 10*/

select l.ruolo ,avg(quotazione) as media from fantacalcio.listone l group by l.ruolo having avg(quotazione) < 10; 


